/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode(int x) : val(x), next(NULL) {}
 * };
 */
class Solution {
public:
    vector<int> reversePrint(ListNode* head) {
        vector<int> ans;

        auto ptr = head;

        while(ptr) {
            ans.push_back(ptr->val);
            ptr = ptr->next;
        }

        return vector<int>(ans.rbegin(), ans.rend());
    }
};
