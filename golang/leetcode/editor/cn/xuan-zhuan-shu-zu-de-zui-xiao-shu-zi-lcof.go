func minArray(numbers []int) int {
	if len(numbers) == 0 {
		return 0
	}

	k := len(numbers) - 1
	for k > 0 && numbers[k] >= numbers[0] {
		k--
	}
	if numbers[k] >= numbers[0] {
		return numbers[0]
	}

	left, right := 0, k

	for left < right {
		mid := (left + right) >> 1
		if numbers[mid] < numbers[0] {
			right = mid
		}else {
			left = mid + 1
		}
	}
	return numbers[left]
}
